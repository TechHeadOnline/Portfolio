function controls() {
  //accelleration#
  if (contros[0]) {
    if (contro.pressing('rightTrigger')) {
      player.speed = 2;
      // var direction = Math.atan2(contro.leftStick.y, contro.leftStick.x);//calculate correct turn
    }

    var direction = Math.atan2(contro.leftStick.y, contro.leftStick.x);
    player.rotation = (direction * 180 / Math.PI);
    player.direction = player.rotation

    //braking / slam on
    if (contro.pressing('leftTrigger')) {
      player.drag = 10
      player.friction = 10
    }
    else {
      player.drag = 5
      player.friction = 5
    }
  }
  else {
    if (kb.pressing('w')) {
      player.speed = 2;
      player.direction = player.rotation
    }
    if (kb.pressing('a')) {
      player.rotate(-5, 5)
      player.direction = player.rotation
    }
    if (kb.pressing('d')) {
      player.rotate(5, 5)
      player.direction = player.rotation
    }
    if (kb.pressing('s')) {
      player.speed = -2;
    }
  }
}


//functions to move the other car
function aiMove() {
  for (c of otherCar) {
    if (node[c.counter]) {
      c.moveTo(node[c.counter], c.mph / 5)
      c.rotateMinTo(node[c.counter], 10, 0)
      for (p of node) {
        if (c.overlaps(p)) {
          c.counter += 1;
        }
      }
    }
    else {
      c.counter = 0
    }
  }
}







