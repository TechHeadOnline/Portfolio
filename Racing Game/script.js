function draw() {
  clear()
  background(0)
  camera.zoom = 3
  camera.x = player.x
  camera.y = player.y

  camera.on()
  image(bgImg, 0, 0)
  allSprites.draw()
  for (c of otherCar) {
    c.draw()
  }
  if (timer == 0) {
    controls()
    aiMove()
    camera.off()
    fill(255, 255, 0, 100)
    rect(width - 300, 100, 200, 100)
    textSize(30)
    fill(255)
    text("laps:" + laps, width - 250, 150)
    camera.on()
  }
  else {
    camera.off()
    drawTimer()
    for (c of otherCar) {
      c.mph = floor(random(3, 10))
      c.image = carImages[Math.floor(Math.random() * carImages.length)]
    }
    camera.on()
  }


}
